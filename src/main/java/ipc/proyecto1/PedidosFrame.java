    private void registrarFacGuia() {
        try {
            int numFactura = codigo++;
            String codigoP = "3jX-";
            String DepOrigen = OrigenDepartamentoComboBox.getSelectedItem().toString();
            String MuniOrigen = OrigenMunicipioComboBox.getSelectedItem().toString();
            String DepDestino = DestinoDepartamentoComboBox.getSelectedItem().toString();
            String MuniDestino = DestinoMunicipioComboBox.getSelectedItem().toString();
            String nit = DatosFacturaComboBox.getSelectedItem().toString();
            String tipoPago = ContraEntregaRadioButton.isSelected() ? "Contra Entrega" : "Tarjeta";
            String tamano = TamañoComboBox.getSelectedItem().toString();
            Double cantidad = Double.valueOf(NumPaquetesTextField.getText());
            
            //Double precioEstandarDouble = Double.valueOf(PrecioEstandarTextField.getText());
           Double total = Double.valueOf(TotalTextField.getText());

            Factura f = new Factura(numFactura, codigoP, DepOrigen, MuniOrigen, DepDestino, MuniDestino,nit, tipoPago,tamano,cantidad,total);
            AppState.usuarioLogeado.agregarDatosFacuturas(f);

            UsuarioFrame usuario = new UsuarioFrame();
            usuario.setVisible(true);
            dispose();
            JOptionPane.showMessageDialog(null, "Tarjeta Registrada!", "Alerta", JOptionPane.WARNING_MESSAGE);

        } catch (HeadlessException | NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ocurrio un error inesperado!", "Alerta", JOptionPane.WARNING_MESSAGE);
        }
    }

