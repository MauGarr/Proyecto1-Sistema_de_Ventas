    private void OrigenDepartamentoComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OrigenDepartamentoComboBoxActionPerformed

        String depOrigen = OrigenDepartamentoComboBox.getSelectedItem().toString();

        if (evt.equals(OrigenDepartamentoComboBox.getSelectedItem())) {
            for (int i = 0; i < Proyecto1.departamentos.size(); i++) {
                Departamentos departamentos = Proyecto1.departamentos.get(i);
                String name = departamentos.getNameDep();
                if (name.equals(depOrigen)) {
                    String code = departamentos.codigoDep;
                    for (int e = 0; e < Proyecto1.municipios.size(); e++) {
                        Municipio municipio = Proyecto1.municipios.get(i);
                        String codeMunDep = municipio.getCodeDep();
                        if (code.equals(codeMunDep)) {
                            OrigenDepartamentoComboBox.removeAllItems();
                            OrigenMunicipioComboBox.addItem(municipio.getNameMunicipio());
                        }
                    }

                }
            }
        }
    }//GEN-LAST:event_OrigenDepartamentoComboBoxActionPerformed

